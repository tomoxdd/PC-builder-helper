def cpu_cooler_checker():
    cpu = input("type in your cpu model (e.g. ryzen 7 7700x, i7-14700k).").lower()
    cooler_tdp = int(input("Type in your cpu coolers max wattage rating (number)."))
    recommended = cpu_cooler_tdp.get(cpu)
    if recommended is None:
        return "This processor is not in the database yet."
    elif isinstance(recommended, str):
        if cooler_tdp == "aio":
            return "Your cooler is sufficient for {cpu}."
        else:
            return recommended
    try: cooler_tdp = int(cooler_tdp)
    except ValueError:
        return "Please enter a valid number for a cpu coolers TDP."
    
    if cooler_tdp >= recommended:
        return "Your cooler is sufficient for " + cpu + " (" + str (recommended) + "W recommended)."
    else: 
        return "Your cpu cooler is NOT sufficient for " + cpu + " (" + str(recommended) + "W recommended)."

def gpu_resolution_checker():
    gpu = input("Type in your gpu model (e.g. rtx 4070 ti, r0x 7800 xt).").lower()
    monitor_resolution = input("Type in your monitor resolution (1080p, 1440p, 4k).").lower()

    resolution = gpu_resolution.get(gpu)
    if resolution is None:
        return "This resolution is not in the database yet."
